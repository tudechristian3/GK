package com.goodkredit.myapplication.responses;

import com.goodkredit.myapplication.bean.PrepaidVoucherTransaction;
import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.util.ArrayList;

/**
 * Created by Ban_Lenovo on 5/25/2017.
 */

public class GetPrepaidVoucherResponse {

    @SerializedName("data")
    @Expose
    private ArrayList<PrepaidVoucherTransaction> data;
    @SerializedName("status")
    @Expose
    private String status;
    @SerializedName("message")
    @Expose
    private String message;

    public ArrayList<PrepaidVoucherTransaction> getData() {
        return data;
    }

    public String getStatus() {
        return status;
    }

    public String getMessage() {
        return message;
    }
}
