package com.goodkredit.myapplication.responses.gknegosyo;

import com.goodkredit.myapplication.model.gknegosyo.Distributor;
import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

import java.util.ArrayList;
import java.util.List;

public class GetDistributorsResponse {

    @SerializedName("data")
    @Expose
    private List<Distributor> getDistributors = new ArrayList<>();
    @SerializedName("status")
    @Expose
    private String status;
    @SerializedName("message")
    @Expose
    private String message;

    public List<Distributor> getGetDistributors() {
        return getDistributors;
    }

    public String getStatus() {
        return status;
    }

    public String getMessage() {
        return message;
    }
}
