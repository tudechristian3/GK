package com.goodkredit.myapplication.responses.profile;

import com.goodkredit.myapplication.model.V2SubscriberDetails;
import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

/**
 * Created by Ban on 09/03/2018.
 */

public class GetSubscriberDetailsResponse {

    @SerializedName("data")
    @Expose
    private V2SubscriberDetails subscriberDetails;
    @SerializedName("status")
    @Expose
    private String status;
    @SerializedName("message")
    @Expose
    private String message;

    public V2SubscriberDetails getSubscriberDetails() {
        return subscriberDetails;
    }

    public String getStatus() {
        return status;
    }

    public String getMessage() {
        return message;
    }
}
