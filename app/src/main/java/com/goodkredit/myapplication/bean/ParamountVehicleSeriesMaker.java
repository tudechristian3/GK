package com.goodkredit.myapplication.bean;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

/**
 * Created by User-PC on 1/19/2018.
 */

public class ParamountVehicleSeriesMaker {
    @SerializedName("VehicleID")
    @Expose
    private String VehicleID;
    @SerializedName("Maker")
    @Expose
    private String Maker;
    @SerializedName("VehicleTypeID")
    @Expose
    private String VehicleTypeID;
    @SerializedName("Series")
    @Expose
    private String Series;



    public ParamountVehicleSeriesMaker(String vehicleID, String maker, String vehicleTypeID, String series) {
        VehicleID = vehicleID;
        Maker = maker;
        VehicleTypeID = vehicleTypeID;
        Series = series;
    }

    public String getVehicleID() {
        return VehicleID;
    }

    public String getMaker() {
        return Maker;
    }

    public String getVehicleTypeID() {
        return VehicleTypeID;
    }

    public String getSeries() {
        return Series;
    }
}
