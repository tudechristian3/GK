package com.goodkredit.myapplication.bean;

import java.io.Serializable;
import java.util.ArrayList;

/**
 * Created by Ban_Lenovo on 1/17/2017.
 */

public class Sponsor implements Serializable {

    private String sponsorID;
    private String sponsorName;
    private String street;
    private String city;
    private String province;
    private String country;
    private String zip;
    private String email;
    private String telephoneNumber;
    private String mobileNo;
    private String sponsorLogo;
    private String websitelink;
    private String description;
    private String longitude;
    private String latitude;


    public Sponsor(String sponsorID, String sponsorName, String street, String city, String province, String country, String zip, String email, String telephoneNumber, String mobileNo, String sponsorLogo, String websitelink, String description, String longitude, String latitude) {
        this.sponsorID = sponsorID;
        this.sponsorName = sponsorName;
        this.street = street;
        this.city = city;
        this.province = province;
        this.country = country;
        this.email = email;
        this.zip = zip;
        this.telephoneNumber = telephoneNumber;
        this.mobileNo = mobileNo;
        this.sponsorLogo = sponsorLogo;
        this.longitude = longitude;
        this.latitude = latitude;
        this.websitelink = websitelink;
        this.description = description;
    }

    public static ArrayList<String> getTitleHeaders() {
        ArrayList<String> titleList = new ArrayList<>();
        titleList.add("PENDING");
        titleList.add("APPROVED");
        return titleList;

    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getSponsorID() {
        return sponsorID;
    }

    public void setSponsorID(String sponsorID) {
        this.sponsorID = sponsorID;
    }

    public String getSponsorName() {
        return sponsorName;
    }

    public void setSponsorName(String sponsorName) {
        this.sponsorName = sponsorName;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelephoneNumber() {
        return telephoneNumber;
    }

    public void setTelephoneNumber(String telephoneNumber) {
        this.telephoneNumber = telephoneNumber;
    }

    public String getMobileNo() {
        return mobileNo;
    }

    public void setMobileNo(String mobileNo) {
        this.mobileNo = mobileNo;
    }

    public String getSponsorLogo() {
        return sponsorLogo;
    }

    public void setSponsorLogo(String sponsorlogo) {
        this.sponsorLogo = sponsorlogo;
    }

    public String getSponsorDescription() {
        return description;
    }

    public void setSponsorDescription(String description) {
        this.description = description;
    }

    public String getWebsitelink() {
        return websitelink;
    }

    public void setWebsitelink(String websitelink) {
        this.websitelink = websitelink;
    }

    public String getLongitude() {
        return longitude;
    }

    public void getLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getLatitude() {
        return latitude;
    }

    public void getLatitude(String latitude) {
        this.latitude = latitude;
    }

}
